const sqlite3 = require('sqlite3');
import path from 'path';
import { PRESET_DOMAINS, DEFAULT_AI_PROMPTS } from './config';

const dbPath = path.join(__dirname, '../data/questions.db');
const db: any = new sqlite3.Database(dbPath);

export const run = (sql: string, params?: any[]): Promise<{ lastID: number; changes: number }> => {
  return new Promise((resolve, reject) => {
    db.run(sql, params, function (this: any, err: Error | null) {
      if (err) reject(err);
      else resolve({ lastID: this.lastID, changes: this.changes });
    });
  });
};

export const get = (sql: string, params?: any[]): Promise<any> => {
  return new Promise((resolve, reject) => {
    db.get(sql, params, (err: Error | null, row: any) => {
      if (err) reject(err);
      else resolve(row);
    });
  });
};

export const all = (sql: string, params?: any[]): Promise<any[]> => {
  return new Promise((resolve, reject) => {
    db.all(sql, params, (err: Error | null, rows: any[]) => {
      if (err) reject(err);
      else resolve(rows);
    });
  });
};

export const initDb = async () => {
  await run(`
    CREATE TABLE IF NOT EXISTS users (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      username TEXT UNIQUE NOT NULL,
      password_hash TEXT NOT NULL,
      created_at DATETIME DEFAULT CURRENT_TIMESTAMP
    )
  `);

  await run(`
    CREATE TABLE IF NOT EXISTS questions (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      category TEXT NOT NULL,
      subcategory TEXT,
      question TEXT NOT NULL,
      answer TEXT NOT NULL,
      difficulty TEXT CHECK(difficulty IN ('easy', 'medium', 'hard')),
      tags TEXT,
      created_at DATETIME DEFAULT CURRENT_TIMESTAMP
    )
  `);

  await run(`
    CREATE TABLE IF NOT EXISTS custom_questions (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      user_id INTEGER NOT NULL,
      category TEXT DEFAULT '自定义',
      subcategory TEXT,
      question TEXT NOT NULL,
      answer TEXT NOT NULL,
      tags TEXT,
      created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
      updated_at DATETIME DEFAULT CURRENT_TIMESTAMP,
      FOREIGN KEY(user_id) REFERENCES users(id)
    )
  `);

  await run(`
    CREATE TABLE IF NOT EXISTS favorites (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      user_id INTEGER NOT NULL,
      question_id INTEGER,
      source_type TEXT CHECK(source_type IN ('system', 'custom')) NOT NULL,
      created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
      FOREIGN KEY(user_id) REFERENCES users(id)
    )
  `);

  await run(`
    CREATE TABLE IF NOT EXISTS ai_conversations (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      user_id INTEGER NOT NULL,
      title TEXT,
      messages TEXT,
      created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
      updated_at DATETIME DEFAULT CURRENT_TIMESTAMP,
      FOREIGN KEY(user_id) REFERENCES users(id)
    )
  `);

  await run(`
    CREATE TABLE IF NOT EXISTS user_progress (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      user_id INTEGER NOT NULL,
      question_id INTEGER NOT NULL,
      is_correct BOOLEAN,
      interaction_type TEXT CHECK(interaction_type IN ('answer', 'ai')),
      answered_at DATETIME DEFAULT CURRENT_TIMESTAMP,
      FOREIGN KEY(user_id) REFERENCES users(id),
      FOREIGN KEY(question_id) REFERENCES questions(id)
    )
  `);

  // Add missing columns for existing user_progress table
  try { await run(`ALTER TABLE user_progress ADD COLUMN interaction_type TEXT CHECK(interaction_type IN ('answer', 'ai'))`); } catch(e) {}
  try { await run(`ALTER TABLE user_progress ADD COLUMN tech_domain TEXT`); } catch(e) {}
  // SM-2 间隔重复字段
  try { await run(`ALTER TABLE user_progress ADD COLUMN self_assessment TEXT CHECK(self_assessment IN ('forgot', 'hazy', 'remembered'))`); } catch(e) {}
  try { await run(`ALTER TABLE user_progress ADD COLUMN next_review_at DATETIME`); } catch(e) {}
  try { await run(`ALTER TABLE user_progress ADD COLUMN review_interval INTEGER DEFAULT 0`); } catch(e) {}
  try { await run(`ALTER TABLE user_progress ADD COLUMN ease_factor REAL DEFAULT 2.5`); } catch(e) {}
  try { await run(`ALTER TABLE questions ADD COLUMN tech_domain TEXT`); } catch(e) {}
  try { await run(`ALTER TABLE custom_questions ADD COLUMN tech_domain TEXT`); } catch(e) {}
  try { await run(`ALTER TABLE favorites ADD COLUMN tech_domain TEXT`); } catch(e) {}

  // AI 提示词配置表
  try { await run(`CREATE TABLE IF NOT EXISTS ai_prompts (
    key TEXT PRIMARY KEY,
    value TEXT NOT NULL,
    description TEXT,
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP
  )`); } catch(e) {}
  const promptCount = await get('SELECT COUNT(*) as cnt FROM ai_prompts');
  if (!promptCount.cnt) {
    for (const d of DEFAULT_AI_PROMPTS) {
      await run('INSERT INTO ai_prompts (key, value, description) VALUES (?, ?, ?)', [d.key, d.value, d.description]);
    }
  }

  // 种子题目：仅当没有新领域题目时执行
  const newDomainCount = await get("SELECT COUNT(*) as count FROM questions WHERE tech_domain IN ('it-tech','general','car','gaokao')");
  if (!newDomainCount.count) {
    // 清除旧的 java 领域题目
    await run("DELETE FROM questions WHERE tech_domain IN ('java','go','python','frontend','database','devops')");
    // 清除旧的 tech_domain 为 NULL 的题目
    await run("DELETE FROM questions WHERE tech_domain IS NULL");
    await seedSampleQuestions();
  }

  // 重建 tech_domains 表为复合主键 (code, user_id)，支持每用户独立领域
  const tableInfo = await get("SELECT sql FROM sqlite_master WHERE name='tech_domains' AND sql NOT LIKE '%PRIMARY KEY (code, user_id)%'");
  if (tableInfo) {
    await run('DROP TABLE IF EXISTS tech_domains');
    await run(`
      CREATE TABLE tech_domains (
        code TEXT NOT NULL,
        name TEXT NOT NULL,
        icon TEXT,
        description TEXT,
        sort_order INTEGER DEFAULT 0,
        is_active INTEGER DEFAULT 1,
        user_id INTEGER,
        PRIMARY KEY (code, user_id)
      )
    `);
    console.log('✅ 重建 tech_domains 表为复合主键');
  }

  // 为已有用户补全默认领域
  const users = await all('SELECT id FROM users');
  for (const u of users) {
    const userDomainCount = await get('SELECT COUNT(*) as cnt FROM tech_domains WHERE user_id = ?', [u.id]);
    if (!userDomainCount.cnt) {
      for (const d of PRESET_DOMAINS) {
        await run(
          'INSERT OR IGNORE INTO tech_domains (code, name, icon, description, sort_order, user_id) VALUES (?, ?, ?, ?, ?, ?)',
          [d.code, d.name, d.icon, d.description, d.sortOrder, u.id]
        );
      }
      console.log(`✅ 为用户 id=${u.id} 补全 ${PRESET_DOMAINS.length} 个默认领域`);
    }
  }
};

